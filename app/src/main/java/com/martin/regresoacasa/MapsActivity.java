    @RequiresApi(api = Build.VERSION_CODES.M)
    @Override



    public void onMapReady(GoogleMap googleMap) {
        mMap = googleMap;

        miUbicacion();


    }

    public void agregarMarcador(double latitud, double longitud) {


        LatLng cord = new LatLng(latitud, longitud);

       CameraUpdate miUbicacion = CameraUpdateFactory.newLatLngZoom(cord, 16);
        if (marcador != null) marcador.remove();
        marcador = mMap
                .addMarker(new MarkerOptions().position(cord).title("Aqui estoy"));

        mMap.animateCamera(miUbicacion);








        String url ="https://maps.googleapis.com/maps/api/directions/json?origin="+latitud+","+longitud+"&destination=20.1394083,-101.1507207";

        RequestQueue queue = Volley.newRequestQueue(this);
        StringRequest stringRequest = new StringRequest(Request.Method.GET, url, new Response.Listener<String>() {
            @Override
            public void onResponse(String response) {


                try {
                    jso = new JSONObject(response);
                    trazarRuta(jso);
                    Log.i("jsonRuta: ",""+response);

                } catch (JSONException e) {
                    e.printStackTrace();
                }

            }
        }, new Response.ErrorListener() {
            @Override
            public void onErrorResponse(VolleyError error) {

            }
        });

        queue.add(stringRequest);





    }






    private void trazarRuta(JSONObject jso) {
        String s = jso.toString();
        Toast.makeText(this,s,Toast.LENGTH_LONG).show();
        JSONArray jRoutes;
        JSONArray jLegs;
        JSONArray jSteps;

        try {
            jRoutes = jso.getJSONArray("routes");
            for (int i=0; i<jRoutes.length();i++){

                jLegs = ((JSONObject)(jRoutes.get(i))).getJSONArray("legs");

                for (int j=0; j<jLegs.length();j++){

                    jSteps = ((JSONObject)jLegs.get(j)).getJSONArray("steps");

                    for (int k = 0; k<jSteps.length();k++){


                        String polyline = ""+((JSONObject)((JSONObject)jSteps.get(k)).get("polyline")).get("points");
                        Log.i("end",""+polyline);
                        List<LatLng> list = PolyUtil.decode(polyline);
                        mMap.addPolyline(new PolylineOptions().addAll(list).color(Color.GRAY).width(5));



                    }



                }



            }

        } catch (JSONException e) {
            e.printStackTrace();
        }




        }





    public void actualizarUbicacion(Location ubicacion) {

        if (ubicacion != null) {

            latitud = ubicacion.getLatitude();
            longitud = ubicacion.getLongitude();
            agregarMarcador(latitud, longitud);

        }


    }

